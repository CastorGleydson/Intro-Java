// Nessa aula, trabalharemos com com ceito de orpradores relacionai.
// ou seja, os operados, que fazem relações de uma coi com outra, de uma varaiaval com outra.
// para isso teremos os seguintes operados relacionados abaixo:
//Maior: >;
//Menor: <;
//Igual: ==;
//Diferente: !=;

public class Aula4_OPeradoresRelacionais {
    public static void main(String[] args) {
        int x = 5, y = 3;
        System.out.println(x > y);
        System.out.println(x != y);
        System.out.println(x < y);
        System.out.println(x == y);
    }
}
