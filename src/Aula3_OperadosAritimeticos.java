//Nessa aula trabalharemos com os operadores aritimeticos básicos em Java.
//Para isso seguiremos como os seguintes operadosres abaixo.
//Soma +;
//Subtração -;
//Muitiplicação *;
//Divisão /;
//Resto da divisão %;

public class Aula3_OperadosAritimeticos {
    public static void main(String[] args) {
        int a = 10, b = 3;
        System.out.println("Soma: "+ (a + b));
        System.out.println("Subtração: "+ (a - b));
        System.out.println("Mutiplicação: "+ (a * b));
        System.out.println("Divisão: "+ (a / b));
        System.out.println("Resto: "+ (a % b));
    }
}
